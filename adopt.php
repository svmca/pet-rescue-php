<h3>Rehoming Policies and Procedures</h3>
<p>We take in a variety of dogs and cats at Pet Resuce NI. Every animal is unique and will have their own specific as well as general needs.<p>

<p>We do not rule out areas or postcodes.</p>

<p>Most of our dogs have unknown history and therefore will not be rehomed with very young children.</p>

<p>Points to consider:
How active are you/your family?
What are you working hours?
How much room have you got?
Where will your new friend sleep?
Is your home environment noisy/quiet?
If you live in rented accommodation have you checked your tenancy agreement, are you allowed pets?
Pups will likely chew!!</p>

<h4>Rehoming Procedure</h4>
<ol>
  <li>If you see an animal you are interested in you should send an email</li>
  <li>Within 24 hours we will send you back a short list of questions. These should be filled in fully with as much information as possible and returned.</li> 
  <li>Please be patient as we all have full time jobs and families outside the rescue. If for any reason you don't receive a reply drop us a message.</li> 
  <li>If you meet the criteria for the particular animal you will be invited by appointment to meet. (Allow around an hour) </li>
  <li>At this stage if you decide to offer a home and we feel the animal will be a good match we will ask you to fill in some more forms. This will allow us to do a home visit. </li>
  <li>During your home visit we will have a look at:
    <ul>
      <li>Your garden, is it secure?</li>
      <li>Where your new pet will be sleeping?</li>
      <li>We are not sent there to see how clean your home is so don't panic!!</li>
    </ul>
  </li>
  <li>After your home visit, if successful you will be sent an email with all your new friends details.</li>
  <li>We would ask that after a home visit you will be ready to adopt within 48 hours.</li> 
</ol>

<p>Most importantly we ask that you keep in touch. All our wee ones are very important and we really love to receive updates.</p>
